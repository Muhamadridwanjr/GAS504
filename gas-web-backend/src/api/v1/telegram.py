"""
GAS Telegram Bot Integration API
- POST /telegram/link     — user links their Telegram account to GAS (authenticated)
- GET  /telegram/me       — check link status
- POST /bot/analyze       — legacy endpoint (all flows, same pipeline)
- POST /bot/signal        — Signal flow: indicator + strategy-core signal + calendar warning
- POST /bot/analysis      — Analisa flow: strategy-core technical + calendar + correlation
- POST /bot/analyst       — Analyst flow: strategy-core hybrid + fundamental + backtest summary
"""
from fastapi import APIRouter, Depends, HTTPException, Header, Request
from pydantic import BaseModel
from typing import Optional, List
import os
import asyncio
import redis.asyncio as aioredis
import httpx

from ...core.dependencies import get_current_user_info
from .billing import deduct_credits, get_user_credits
from .analysis import STYLE_MATRIX, STRATEGY_CORE_URL, KIMI_API_KEY, KIMI_BASE_URL, KIMI_MODEL

router = APIRouter(tags=["Telegram Bot"])

REDIS_URL = os.getenv("REDIS_URL", "redis://gas-redis:6379/0")
BOT_API_KEY = os.getenv("BOT_API_KEY", "gas-bot-key-2026-secret")

_redis = None


async def _get_redis() -> aioredis.Redis:
    global _redis
    if _redis is None:
        _redis = aioredis.from_url(REDIS_URL, decode_responses=True)
    return _redis


def _verify_bot_key(x_bot_key: Optional[str] = Header(None)):
    if x_bot_key != BOT_API_KEY:
        raise HTTPException(status_code=401, detail="Invalid bot API key")
    return True


# ── User: link Telegram account ───────────────────────────────────────────

class LinkRequest(BaseModel):
    code: str  # 8-char code generated by bot


@router.post("/telegram/link")
async def link_telegram(body: LinkRequest, user_info: dict = Depends(get_current_user_info)):
    """
    Authenticated user submits the link code from the Telegram bot.
    Links their Telegram ID to their GAS account in Redis.
    """
    r = await _get_redis()
    tg_user_id = await r.get(f"tg:link:{body.code}")
    if not tg_user_id:
        raise HTTPException(status_code=400, detail="Kode tidak valid atau sudah kedaluwarsa (15 menit)")

    gas_user_id = user_info["user_id"]
    # Store permanent binding
    await r.set(f"tg:bind:{tg_user_id}", gas_user_id)
    # Remove the one-time link code
    await r.delete(f"tg:link:{body.code}")

    return {
        "ok": True,
        "message": "Akun Telegram berhasil dihubungkan ke GAS!",
        "tg_user_id": tg_user_id,
        "gas_user_id": gas_user_id,
    }


@router.get("/telegram/me")
async def telegram_link_status(user_info: dict = Depends(get_current_user_info)):
    """Check if the current user has a linked Telegram account."""
    r = await _get_redis()
    gas_user_id = user_info["user_id"]
    # Search for any tg:bind that maps to this gas_user_id
    # We store tg:bind:{tg_id} = gas_user_id, so scan for the value
    # For efficiency, also store reverse: tg:gas:{gas_user_id} = tg_user_id
    tg_user_id = await r.get(f"tg:gas:{gas_user_id}")
    return {
        "linked": tg_user_id is not None,
        "tg_user_id": tg_user_id,
    }


@router.delete("/telegram/unlink")
async def unlink_telegram(user_info: dict = Depends(get_current_user_info)):
    """Unlink Telegram from this GAS account."""
    r = await _get_redis()
    gas_user_id = user_info["user_id"]
    tg_user_id = await r.get(f"tg:gas:{gas_user_id}")
    if tg_user_id:
        await r.delete(f"tg:bind:{tg_user_id}")
        await r.delete(f"tg:gas:{gas_user_id}")
    return {"ok": True, "message": "Akun Telegram berhasil dilepas."}


