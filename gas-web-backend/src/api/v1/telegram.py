@router.post("/bot/analyze")
async def bot_analyze(
    body: BotAnalyzeRequest,
    authorized: bool = Depends(_verify_bot_key),
):
    """
    Called by gas-telegram-bot to run AI analysis for a linked user.
    Routes to gas-strategy-core for real AI signal generation.
    All plans allowed; plan tier determines AI model quality.
    """
    r = await _get_redis()

    # Read plan (all plans allowed — quality varies by tier)
    plan = await r.get(f"user:{body.gas_user_id}:plan") or "free"

    # Credit cost varies by style
    cost = _STYLE_COST.get(body.style.lower(), 3)
    # Free/trial users pay 1 credit
    if plan in ("free", "trial"):
        cost = 1

    ok, remaining = await deduct_credits(body.gas_user_id, cost, is_admin=False)
    if not ok:
        raise HTTPException(402, detail=f"Kredit tidak cukup. Sisa: {remaining}")

    timeframe = _STYLE_TF.get(body.style.lower(), "H1")

    model_tier = _PLAN_TIER.get(plan.lower(), "basic")

    # Call gas-strategy-core for real AI signal
    try:
        async with httpx.AsyncClient(timeout=30) as client:
            resp = await client.post(
                f"{STRATEGY_CORE_BOT_URL}/v1/analysis/signal",
                json={
                    "pair": body.pair.upper(),
                    "timeframe": timeframe,
                    "style": body.style.lower(),
                    "model_tier": model_tier,
                    "user_id": body.gas_user_id,
                },
            )
            resp.raise_for_status()
            result = resp.json()
    except Exception as e:
        raise HTTPException(503, detail=f"AI engine error: {str(e)[:100]}")

    # Normalise response to match bot formatter expectations
    return {
        "pair": body.pair,
        "style": body.style,
        "grade": result.get("grade", "B"),
        "stars": result.get("stars", 2),
        "probability": result.get("probability", result.get("confidence", 55)),
        "signal": result.get("signal", result.get("direction", "WAIT")),
        "bias": result.get("bias", "Neutral"),
        "entry": result.get("entry", result.get("entry_zone")),
        "stop_loss": result.get("sl", result.get("stop_loss")),
        "tp1": result.get("tp1", result.get("take_profit_1")),
        "tp2": result.get("tp2", result.get("take_profit_2")),
        "tp3": result.get("tp3", result.get("take_profit_3")),
        "rr": result.get("rr", result.get("risk_reward", "1:2")),
        "summary": result.get("summary", result.get("reasoning", "")),
        "session": result.get("session", ""),
        "credits_used": cost,
        "credits_remaining": remaining,
        "source": result.get("source", "ai"),
        "model": result.get("model", ""),
    }


# ── Shared helpers for new bot endpoints ─────────────────────────────────

async def _core_post(endpoint: str, payload: dict, timeout: int = 45) -> dict:
    """Fire-and-forget POST to strategy-core; returns {} on error."""
    try:
        async with httpx.AsyncClient(timeout=timeout) as c:
            r = await c.post(f"{STRATEGY_CORE_BOT_URL}{endpoint}", json=payload)
            r.raise_for_status()
            return r.json()
    except Exception:
        return {}


async def _svc_get(base_url: str, path: str, params: dict = None, timeout: int = 10) -> dict:
    """Fire-and-forget GET to a microservice; returns {} on error."""
    try:
        async with httpx.AsyncClient(timeout=timeout) as c:
            r = await c.get(f"{base_url}{path}", params=params or {})
            r.raise_for_status()
            return r.json()
    except Exception:
        return {}


async def _svc_post(base_url: str, path: str, payload: dict, timeout: int = 15) -> dict:
    """Fire-and-forget POST to a microservice; returns {} on error."""
    try:
        async with httpx.AsyncClient(timeout=timeout) as c:
            r = await c.post(f"{base_url}{path}", json=payload)
            r.raise_for_status()
            return r.json()
    except Exception:
        return {}


def _resolve_tf(style: str) -> str:
    return {"scalping": "M15", "intraday": "H1", "swing": "H4", "position": "D1"}.get(style.lower(), "H1")


def _style_tfs(style: str) -> list:
    return STYLE_MATRIX.get(style.lower(), {}).get("tfs", ["H1"])


# ── NEW: /bot/signal ──────────────────────────────────────────────────────────

class BotFlowRequest(BaseModel):
    gas_user_id: str
    pair: str
    style: str
    ai_tier: str = "basic"


@router.post("/bot/signal")
async def bot_signal(
    body: BotFlowRequest,
    authorized: bool = Depends(_verify_bot_key),
):
    """
    Signal flow — fast entry signal.
    Pipeline: strategy-core /signal + calendar-news high-impact check.
    Returns: standard signal card + calendar_warning field.
    """
    r = await _get_redis()
    plan = await r.get(f"user:{body.gas_user_id}:plan") or "free"
    cost = _STYLE_COST.get(body.style.lower(), 3)
    if plan in ("free", "trial"):
        cost = 1

    ok, remaining = await deduct_credits(body.gas_user_id, cost, is_admin=False)
    if not ok:
        raise HTTPException(402, detail=f"Kredit tidak cukup. Sisa: {remaining}")

    tf       = _resolve_tf(body.style)
    tf_mtx   = _style_tfs(body.style)
    tier     = _PLAN_TIER.get(plan.lower(), "basic")

    # Run signal + calendar in parallel
    signal_task  = _core_post("/v1/analysis/signal", {
        "pair": body.pair.upper(), "timeframe": tf,
        "style": body.style.lower(), "timeframe_matrix": tf_mtx,
        "model_tier": tier, "user_id": body.gas_user_id,
    }, timeout=45)
    calendar_task = _svc_get(CALENDAR_NEWS_URL, "/calendar", {"limit": 5})

    signal_data, cal_data = await asyncio.gather(signal_task, calendar_task)

    if not signal_data:
        raise HTTPException(503, detail="Signal engine unavailable")

    # Extract calendar warning for high-impact events
    events      = cal_data.get("events", []) if cal_data else []
    hi_events   = [e for e in events if str(e.get("impact", "")).lower() == "high"]
    cal_warning = ""
    if hi_events:
        titles = ", ".join(e.get("title", e.get("event", "?"))[:30] for e in hi_events[:2])
        cal_warning = f"⚠️ {len(hi_events)} high-impact event hari ini: {titles}"

    return {
        "pair":              body.pair.upper(),
        "style":             body.style,
        "pipeline":          "signal",
        "grade":             signal_data.get("grade", "B"),
        "stars":             signal_data.get("stars", 2),
        "probability":       signal_data.get("probability", signal_data.get("confidence", 55)),
        "signal":            signal_data.get("signal", signal_data.get("direction", "WAIT")),
        "bias":              signal_data.get("bias", "Neutral"),
        "entry":             signal_data.get("entry", signal_data.get("entry_zone")),
        "stop_loss":         signal_data.get("sl", signal_data.get("stop_loss")),
        "tp1":               signal_data.get("tp1"),
        "tp2":               signal_data.get("tp2"),
        "tp3":               signal_data.get("tp3"),
        "rr":                signal_data.get("rr", signal_data.get("risk_reward", "1:2")),
        "summary":           signal_data.get("summary", signal_data.get("reasoning", "")),
        "session":           signal_data.get("session", ""),
        "calendar_warning":  cal_warning,
        "credits_used":      cost,
        "credits_remaining": remaining,
        "model":             signal_data.get("model", ""),
        "source":            signal_data.get("source", "ai"),
    }


