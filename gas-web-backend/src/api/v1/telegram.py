STRATEGY_CORE_BOT_URL   = os.getenv("STRATEGY_CORE_URL",          "http://gas-strategy-core:7003")
CALENDAR_NEWS_URL       = os.getenv("CALENDAR_NEWS_URL",          "http://gas-calendar-news-service:9601")
